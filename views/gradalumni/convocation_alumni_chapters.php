        <div style="text-align: justify;">
            The UMP Convocation was established on 4 August 2018 according to the Constitution of the Convocation and the Statute of UMP. The Convocation members were welcomed by the Vice-Chancellor, Professor Thoko Mayekiso, who defined Convocation as “a group of people formally assembled for a specific purpose-mostly ecclesiastical or academic”.
        </div>
        <br />
        <strong>The functions of the Convocation are to:</strong>
        <ul>
            <li style="text-align: justify;">Facilitate liaison between the University and its former students and its former students and academic employees;</li>
            <li style="text-align: justify;">Create networks and canvass support for the strategic objectives of the University;</li>
            <li style="text-align: justify;">Organise former students and academic staff members of the University with a view to facilitating representation of this constituency on appropriate governance structures.</li>
        </ul>

